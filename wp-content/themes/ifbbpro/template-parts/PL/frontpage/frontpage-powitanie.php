         <p>Witam Cię na oficjalnej stronie Międzynarodowego Festiwalu Sportów Sylwetkowych Federacji NPC Worldwide & IFBB Professional League. </p>

         <p>Zapraszam Cię na kolejne pięć edycji Festiwalu, które odbędą się w kwietniu, maju, sierpniu, wrześniu oraz październiku 2021 roku w Warszawie.</p>

         <p>W trakcie każdej edycji Festiwalu będą odbywały się ponownie targi branżowe, które mają na celu zaprezentowanie bogatej gamy sprzętu, produktów, usług i akcesoriów fitness & wellness naszych partnerów przed szerokim gronem pasjonatów sportów sylwetkowych z Polski i z zagranicy. Najważniejszym wydarzeniem sportowym każdej edycji Festiwalu są światowej rangi mistrzostwa ligi amatorskiej NPC Worldwide oraz mistrzostwa ligi zawodowej IFBB Professional League, w których udział wezmą zawodnicy z całego świata. Fani sportów sylwetkowych będą mieli niepowtarzalną okazję spotkania międzynarodowych gwiazd, najlepszych trenerów i wielu swoich idoli sportowych.</p>

         <p>Zawodnicy biorący udział w konkursach amatorskich NPC Worldwide oraz mistrzostwach ligi zawodowej IFBB Professional League w roku 2021 będą walczyli o:</p>

         <ul>
             <li><b>☆</b> 6 kwalifikacji umożliwiających udział w najważniejszym konkursie dla profesjonalistów, zawodach Mr. Olympia w USA w roku 2021 i 2022.</li>
             <li><b>☆</b> 27 kart zawodowych IFBB PRO League umożliwiających udział w najbardziej prestiżowych zawodach na całym świecie.</li>
             <li><b>☆</b> Prestiżowe tytuły mistrzowskie federacji NPC Worldwide oraz IFBB Professional League.</li>
             <li><b>☆</b> Kwalifikacje do reprezentacji narodowej swojego kraju na Mistrzostwa Świata NPC Worldwide w USA.</li>
             <li><b>☆</b> Nagrody finansowe ufundowane przez organizatora i promotora zawodów IFBB Professional League dla wszystkich finalistów.</li>
             <li><b>☆</b> Nagrody rzeczowe ufundowane przez partnerów i sponsorów zawodów NPC Worldwide.</li>
             <li><b>☆</b> Sponsorowane starty w zawodach rangi PRO QUALIFIER.</li>
         </ul>

         <p>W amatorskich konkursach federacji NPC Worldwide mogą startować zarówno zawodnicy niezrzeszeni, jak i zawodnicy ze wszystkich krajowych i międzynarodowych federacji bez żadnych ograniczeń, kwalifikacji i zezwoleń. Aby wziąć udział w naszych konkursach należy posiadać wykupioną licencję zawodnika NPC Worldwide na dany rok kalendarzowy. W trakcie każdej edycji Festiwalu zawodnik ma prawo startu w dowolnej ilości kategorii (Beginner, Novice, Junior, Master, Open), o ile spełnia wymogi danej kategorii. W każdej z tych pięciu kategorii ma on prawo startu w dwóch dowolnie wybranych konkurencjach sylwetkowych (dywizjach) z poniżej wymienionych:</p>

