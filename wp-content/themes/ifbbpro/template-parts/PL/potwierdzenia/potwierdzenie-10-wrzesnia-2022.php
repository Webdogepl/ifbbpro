        <h3>Harmonogram i miejsce zawodów</h3>
    </a>

    <p>Witamy serdecznie,<br>
        Twoje zgłoszenie na zawody NPC w Kulturystyce i Fitness 2022 zostało przyjęte. Niniejszym potwierdzam wpisanie Cię na listę startową zawodników biorących udział w zawodach.</p>


    <p>Proszę o punktualny przyjazd na weryfikację, która odbędzie się w piątek 9 września w godzinach 11:00 -20:00.</p>


    <p>Proszę o przyniesienie na weryfikację:</p>
    <ul>
        <li>dokumentu tożsamości</li>
        <li>stroju startowego</li>
        <li>pendrive z nagraniem do programu dowolnego jeśli jest wymagany w danej konkurencji (utwór powinien być nagrany jako pierwszy, pen drive podpisany i sprawdzony po nagraniu przez zawodnika)</li>
        <li>wydrukowanego na kartce lub zapisanego w urządzeniu mobilnym potwierdzenia opłaty wpisowego z danymi zawodnika</li>
        <li>wydrukowanego na kartce lub zapisanego w urządzeniu mobilnym potwierdzenia przyjęcia rejestracji i zgłoszenia zawodnika na zawody czyli maila, który otrzymałeś na podany adres</li>
        <li>licencji NPC w formie cyfrowej lub wydruku</li>
    </ul>

    <p>
        Dziękuję za Twoje zgłoszenie.<br> Życzę Ci przygotowania życiowej formy startowej i do zobaczenia na Festiwalu.
    </p>

